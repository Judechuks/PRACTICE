// Categories of Operators
// ==========================
let a, b, c, d, e, f, g;

// 1. Assignment(Assign a value) =
a = 10;
// 2. Arithmetic + - * /
a + b * c / d;

// 3. Increment '++'
b = 1;
console.log('Before Increment b = '+b++); //evaluate to 1 because it is displayed before incremented
// b++;
console.log('After Increment b = '+b++);

// 4. Decrement '--'
c = 2;
console.log('Before Decrement c = '+c--); //evaluate to 2, because it is displayed before decremented
// c--;
console.log('After Decrement c = '+c--);

// 5.  String Concatenator '+'
console.log("Monye"+"Jude");

// 6. Precedence '(, ), *, /, +, _ '
a = b + (c + 2) * 3;

// 7. Function Invocator Operator '()'
console.log();

// 8. Logical operator 'AND &&', 'OR ||'

// 9. Member accessor operator '.'
console.log()

// 10. Code block operator '{}'
function (){
  // the block body of the function
}

