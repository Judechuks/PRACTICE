// the values in a variable/ identifier determines the datatype (not the variable itself)

// number
let a = 6;
console.log('datatype: '+typeof a);

// string
let b = 'Hello world';
console.log('datatype: '+typeof b);

// boolean
let c = true;
console.log('datatype: '+typeof c);

// undefined
let d;
console.log('value of '+d);
console.log('datatype: '+typeof d);

//function
function one(){
  return 'one'
}

// object
let e = ['Orange', 'Mango', 'Banana', 'Pineapple'];
console.log('datatype: '+typeof e);
